/** Extract a segment of audio (for per-section Aeneas alignment, FXL-style accuracy). */
async function extractAudioSegment(sourcePath, startSec, durationSec, outPath) {
  const start = Math.max(0, Number(startSec));
  const duration = Math.max(0.1, Number(durationSec));
  const safeSrc = sourcePath.replace(/"/g, '\\"');
  const safeOut = outPath.replace(/"/g, '\\"');
  execSync(
    `"${ffmpegBin}" -y -i "${safeSrc}" -ss ${start} -t ${duration} -acodec pcm_s16le -ar 16000 -ac 1 "${safeOut}"`,
    { stdio: 'pipe', timeout: 60000, env: getAugmentedEnv() }
  );
}

