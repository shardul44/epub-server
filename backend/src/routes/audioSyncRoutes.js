/**
 * Resolve absolute path to the job's audio file (for reflowable Sync Studio).
 * Uploaded audio takes precedence over TTS so user uploads are not overridden.
 * Order: sync record (if file exists) → uploaded_audio_<jobId> → TTS combined_audio_<jobId>.
 */
async function resolveJobAudioPath(jobId) {
  const id = jobId != null ? String(jobId).trim() : '';
  if (!id) return null;
  const job = await ConversionJobModel.findById(parseInt(id, 10));
  if (!job) return null;
  const audioDir = path.join(getUploadDir(), 'audio');
  const ttsDir = getTtsOutputDir();

  // Check activeAudioSource preference stored in intermediate_data
  // When TTS is generated we set this to 'tts' so uploaded file is bypassed even if it still exists on disk
  let activeAudioSource = null;
  try {
    const intermediate = typeof job.intermediate_data === 'string'
      ? JSON.parse(job.intermediate_data)
      : (job.intermediate_data || {});
    activeAudioSource = intermediate?.activeAudioSource ?? null; // 'tts' | 'uploaded' | null
  } catch (_) { }

  const uploadedPath = path.join(audioDir, `uploaded_audio_${id}.mp3`);
  const ttsCandidates = [
    path.join(ttsDir, `combined_audio_${id}.mp3`),
    path.join(audioDir, `combined_audio_${id}.mp3`),
    path.join(getUploadDir(), 'tts_audio', `combined_audio_${id}.mp3`),
    path.join(audioDir, `audio_${id}.mp3`)
  ];

  // If TTS was explicitly set as the active source, skip uploaded and prefer TTS
  if (activeAudioSource === 'tts') {
    for (const p of ttsCandidates) {
      try { await fs.access(p); return p; } catch (_) { }
    }
    // TTS file not found — fall through to uploaded as last resort
  }

  // 1) Uploaded file — only preferred when activeAudioSource is 'uploaded' or when no TTS file exists
  if (activeAudioSource !== 'tts') {
    // When no preference is set, check if TTS also exists — if so, prefer TTS
    // (TTS is intentionally generated by the user and should win over a previously uploaded file)
    if (activeAudioSource === null) {
      let ttsExists = false;
      for (const p of ttsCandidates) {
        try { await fs.access(p); ttsExists = true; break; } catch (_) { }
      }
      if (ttsExists) {
        // Both exist, no preference set → TTS wins (most recently intentional action)
        for (const p of ttsCandidates) {
          try { await fs.access(p); return p; } catch (_) { }
        }
      }
    }
    try {
      await fs.access(uploadedPath);
      return uploadedPath;
    } catch (_) { }
  }

  // 2) Path from existing sync record
  const existingSyncs = await AudioSyncModel.findByJobId(jobId);
  if (existingSyncs.length > 0 && existingSyncs[0].audio_file_path) {
    let p = existingSyncs[0].audio_file_path;
    if (!path.isAbsolute(p)) {
      p = p.replace(/^(audio[\\/])+/i, '');
      p = path.join(getUploadDir(), 'audio', p);
    }
    try { await fs.access(p); return p; } catch (_) { }
  }

  // 3) TTS-generated combined audio
  for (const p of ttsCandidates) {
    try { await fs.access(p); return p; } catch (_) { }
  }

  // 4) Last resort: uploaded even if source preference says TTS but TTS is missing
  try { await fs.access(uploadedPath); return uploadedPath; } catch (_) { }

  return null;
}

/** Helper: update activeAudioSource flag in job's intermediate_data */
async function setJobActiveAudioSource(jobId, source) {
  try {
    const job = await ConversionJobModel.findById(parseInt(jobId, 10));
    if (!job) return;
    let intermediate = {};
    try {
      intermediate = typeof job.intermediate_data === 'string'
        ? JSON.parse(job.intermediate_data)
        : (job.intermediate_data || {});
    } catch (_) { }
    intermediate.activeAudioSource = source; // 'tts' | 'uploaded'
    await ConversionJobModel.update(parseInt(jobId, 10), {
      intermediateData: JSON.stringify(intermediate)
    });
    console.log(`[AudioSource] Job ${jobId} activeAudioSource set to '${source}'`);
  } catch (err) {
    console.warn(`[AudioSource] Could not update activeAudioSource for job ${jobId}:`, err?.message);
  }
}

